  void _showNewsDetails(UserNewsDTO news) {
    // Haber g√∂r√ºnt√ºleme kaydƒ±nƒ± tut
    NewsService().recordNewsView(news.id);
    
    // Video i√ßeren bir haber i√ßin, eƒüer thumbnail varsa, video oynatƒ±cƒ±yƒ± a√ßabiliriz
    if (news.videoUrl != null && news.videoUrl!.isNotEmpty && 
        news.thumbnailUrl != null && news.thumbnailUrl!.isNotEmpty) {
      _playVideo(news);
    } else {
      // Normal haber detay sayfasƒ±na git
      Navigator.push(
        context,
        MaterialPageRoute(builder: (context) => NewsDetailScreen(news: news)),
      );
    }
  }
  
  void _playVideo(UserNewsDTO news) {
    // Kullanƒ±cƒ±ya normal detay sayfasƒ± veya video oynatƒ±cƒ± se√ßeneƒüi sun
    showModalBottomSheet(
      context: context,
      backgroundColor: Colors.transparent,
      builder: (context) {
        return Container(
          decoration: const BoxDecoration(
            color: Colors.white,
            borderRadius: BorderRadius.vertical(top: Radius.circular(20)),
          ),
          child: Column(
            mainAxisSize: MainAxisSize.min,
            children: [
              ListTile(
                leading: const Icon(Icons.article),
                title: const Text('Haber Detayƒ±nƒ± G√∂r√ºnt√ºle'),
                onTap: () {
                  Navigator.pop(context); // Sheet kapat
                  Navigator.push(
                    context,
                    MaterialPageRoute(builder: (context) => NewsDetailScreen(news: news)),
                  );
                },
              ),
              ListTile(
                leading: const Icon(Icons.play_circle_filled),
                title: const Text('Videoyu Oynat'),
                onTap: () {
                  Navigator.pop(context); // Sheet kapat
                  
                  // Video oynatƒ±cƒ±yƒ± tam ekran olarak a√ß
                  showModalBottomSheet(
                    context: context,
                    isScrollControlled: true,
                    backgroundColor: Colors.transparent,
                    builder: (context) => Container(
                      height: MediaQuery.of(context).size.height * 0.75,
                      decoration: const BoxDecoration(
                        color: Colors.white,
                        borderRadius: BorderRadius.vertical(top: Radius.circular(20)),
                      ),
                      child: Column(
                        children: [
                          // Kapatma √ßubuƒüu
                          Container(
                            width: 50,
                            height: 5,
                            margin: const EdgeInsets.symmetric(vertical: 10),
                            decoration: BoxDecoration(
                              color: Colors.grey[300],
                              borderRadius: BorderRadius.circular(10),
                            ),
                          ),
                          // Video ba≈ülƒ±ƒüƒ±
                          Padding(
                            padding: const EdgeInsets.all(16.0),
